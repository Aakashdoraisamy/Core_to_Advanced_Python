s = "  Hello   World Python  "
result = s.replace(" ", "")
print(result)   # HelloWorldPython

s = "  Hello   World Python  "
result = s.strip()
print(result)   # Hello   World Python


s = "  Hello   World Python  "
result = s.lstrip()
print(result)   # Hello   World Python  


s = "  Hello   World Python  "
result = s.rstrip()
print(result)   #   Hello   World Python


